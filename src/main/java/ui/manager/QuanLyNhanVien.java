public void fillToTable() {
    DefaultTableModel model = (DefaultTableModel) tblBangNhanVien.getModel();
    model.setRowCount(0); 

    String sql = "SELECT * FROM NHANVIEN";

    try {
        ResultSet rs = XJdbc.executeQuery(sql);
        while (rs.next()) {
            Object[] row = new Object[]{
                rs.getInt("MaNV"),
                rs.getNString("TenNV"),
                rs.getString("SDT"),
                rs.getNString("ChucVu"),
                rs.getDouble("Luong"),
                rs.getInt("SoNgayLam"),
                rs.getInt("SoNgayNghi"),
                rs.getString("TenDangNhap")
            };
            model.addRow(row);
        }
        rs.getStatement().getConnection().close(); // Đóng kết nối nếu XJdbc không tự xử lý
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu nhân viên!");
    }
}
public void searchNhanVien() {
    String keyword = txtSearch.getText().trim(); // ô nhập liệu
    DefaultTableModel model = (DefaultTableModel) tblBangNhanVien.getModel();
    model.setRowCount(0); // xóa bảng trước khi load lại

    if (keyword.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Vui lòng nhập mã hoặc tên nhân viên cần tìm!");
        return;
    }

    List<NhanVien> list = new ArrayList<>();
    NhanVienDAOImpl dao = new NhanVienDAOImpl();

    try {
        int maNV = Integer.parseInt(keyword);
        list = dao.selectBySql(
            "SELECT * FROM NHANVIEN WHERE MaNV = ? OR TenNV LIKE ?",
            maNV, "%" + keyword + "%"
        );
    } catch (NumberFormatException e) {
        list = dao.selectBySql(
            "SELECT * FROM NHANVIEN WHERE TenNV LIKE ?",
            "%" + keyword + "%"
        );
    }

    if (list.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Không tìm thấy nhân viên phù hợp!");
        return;
    }

    for (NhanVien nv : list) {
        model.addRow(new Object[]{
            nv.getMaNV(),
            nv.getTenNV(),
            nv.getSdt(),
            nv.getChucVu(),
            nv.getLuong(),
            nv.getSoNgayLam(),
            nv.getSoNgayNghi(),
            nv.getTenDangNhap()
        });
    }
}
public void themNhanVien() {
    String name = txtName.getText().trim();
    String phone = txtPhone.getText().trim();
    String chucVu = txtChucVu.getText().trim();
    String luong = txtLuong.getText().trim();
    String ngayLam = txtNgayLam.getText().trim();
    String ngayNghi = txtNgayNghi.getText().trim();
    String tenDangNhap = txtTenDangNhap.getText().trim();

    if (name.isEmpty() || phone.isEmpty() || chucVu.isEmpty()
            || luong.isEmpty() || ngayLam.isEmpty() || ngayNghi.isEmpty() || tenDangNhap.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
        return;
    }

    try {
        String sql = "INSERT INTO NhanVien (TenNV, Sdt, ChucVu, Luong, SoNgayLam, SoNgayNghi, TenDangNhap) "
                   + "VALUES (?, ?, ?, ?, ?, ?, ?)";
        int rows = XJdbc.update(sql, name, phone, chucVu,
                                Double.parseDouble(luong),
                                Integer.parseInt(ngayLam),
                                Integer.parseInt(ngayNghi),
                                tenDangNhap);

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Thêm nhân viên vào CSDL thành công!");
            fillToTable(); // Load lại bảng hiển thị
            btnLamMoi.doClick(); // Xoá form
        }
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi thêm vào CSDL!");
    }
}

public void capNhatNhanVien() {
    int selectedRow = tblBangNhanVien.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần cập nhật!");
        return;
    }

    String id = txtID.getText().trim();
    String phone = txtPhone.getText().trim();
    String chucVu = txtChucVu.getText().trim();
    String luong = txtLuong.getText().trim();

    if (phone.isEmpty() || chucVu.isEmpty() || luong.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Không được để trống các trường được chỉnh sửa!");
        return;
    }

    try {
        String sql = "UPDATE NhanVien SET Sdt = ?, ChucVu = ?, Luong = ? WHERE MaNV = ?";
        int rows = XJdbc.update(sql, phone, chucVu, Double.parseDouble(luong), id);

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
            fillToTable(); // Load lại bảng
        }
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật vào CSDL!");
    }
}
public void xoaNhanVien() {
    String id = txtID.getText().trim();

    if (id.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn nhân viên cần xóa!");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(this,
            "Bạn có chắc muốn xóa nhân viên có mã: " + id + "?", 
            "Xác nhận", 
            JOptionPane.YES_NO_OPTION);

    if (confirm != JOptionPane.YES_OPTION) {
        return;
    }

    try {
        String sql = "DELETE FROM NhanVien WHERE MaNV = ?";
        int rows = XJdbc.update(sql, Integer.parseInt(id));

        if (rows > 0) {
            JOptionPane.showMessageDialog(this, "Xóa nhân viên thành công!");
            fillToTable(); // Refresh bảng
            btnLamMoi.doClick(); // Reset biểu mẫu
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy nhân viên để xóa.");
        }
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Lỗi khi xóa nhân viên!");
    }
}


