    private void tblBangNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBangNhanVienMouseClicked
        // TODO add your handling code here:
    tblBangNhanVien.addMouseListener(new MouseAdapter() {
    @Override
    public void mouseClicked(MouseEvent e) {
        int selectedRow = tblBangNhanVien.getSelectedRow();
        if (selectedRow != -1) {
            txtID.setText(tblBangNhanVien.getValueAt(selectedRow, 0).toString());
            txtName.setText(tblBangNhanVien.getValueAt(selectedRow, 1).toString());
            txtPhone.setText(tblBangNhanVien.getValueAt(selectedRow, 2).toString());
            txtChucVu.setText(tblBangNhanVien.getValueAt(selectedRow, 3).toString());
            txtLuong.setText(tblBangNhanVien.getValueAt(selectedRow, 4).toString());
            txtNgayLam.setText(tblBangNhanVien.getValueAt(selectedRow, 5).toString());
            txtNgayNghi.setText(tblBangNhanVien.getValueAt(selectedRow, 6).toString());
            txtTenDangNhap.setText(tblBangNhanVien.getValueAt(selectedRow, 7).toString());

            // Khóa các trường không cho phép sửa
            txtID.setEditable(false);
            txtName.setEditable(false);
            txtNgayLam.setEditable(false);
            txtNgayNghi.setEditable(false);
            txtTenDangNhap.setEditable(false);

            // Mở các trường được phép sửa
            txtPhone.setEditable(true);
            txtChucVu.setEditable(true);
            txtLuong.setEditable(true);

            // ✅ Chuyển qua tab "Biểu Mẫu"
            TabbedPane.setSelectedIndex(1);  // <-- CHỈNH LẠI THÀNH 1
        }
    }
});

    }//GEN-LAST:event_tblBangNhanVienMouseClicked

